#include<bits/stdc++.h>

using namespace std;

int main() {
    char ch; cin >> ch;
    int n; cin >> n;

    for ( int i = 0 ; i < n ; i++ ) {
        int a;
        cin >> a;

        for ( int j = 0; j < a ; j++ ) {
            cout << ch;
        }
        cout << endl;
    }
    cout << endl;
}